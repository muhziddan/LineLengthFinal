package com.minibootcamp.ada;

public class LineLength {

    public double calculateZeroLengthLine() {
        return 0;
    }

    public double calculateHorizontalFromOrigin(double x1, double x2) {
        return x2 - x1;
    }

    public double calculateVerticalFromOrigin(double y1, double y2) {
        return y2 - y1;
    }
}
